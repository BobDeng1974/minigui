struct EColor{
#ifdef EG_COMPACT_ECOLOR
/*
    	[R4R3R2R1R0][G5G4G3 G2G1G0][B4B3B2B1B0]
                    HighByte           |         Low Byte
*/
	EColor(unsigned char lowByte=0,unsigned char highByte=0)
		:m_cLowByte(lowByte),m_cHighByte(highByte){}
	EColor(unsigned char r,unsigned char g,unsigned char b){
		unsigned int temp,res;
		//delete low bits.
		r=r>>3;
		g=g>>2;
		b=b>>3;
		temp=r<<11;
		res=b+temp;
		temp=g<<5;
		res=res+temp;
		m_cLowByte=(unsigned char)(res&0xFF);
		temp=res>>8;
		m_cHighByte=(unsigned char)(temp&0xFF);
	}
	unsigned char GetRed(){
		return m_cHighByte&0xF8;
	}
	unsigned char GetGreen(){
		unsigned char temp=m_cHighByte;
		temp=temp&0x7;
		temp=temp<<5;
		return temp|((m_cLowByte>>3)&0x1C);
	}
	unsigned char GetBlue(){
		return (m_cLowByte&0x1F)<<3;		
	}
	void SetRed(unsigned char red){
		red=red&0xF8;
		m_cHighByte=m_cHighByte&0x07;
		m_cHighByte=m_cHighByte|red;
	}
	void SetGreen(unsigned char green){
		unsigned char temp=green>>5;
		temp=temp&0x07;
		m_cHighByte=m_cHighByte&0xF8;
		m_cHighByte=m_cHighByte|temp;
		temp=green<<3;
		temp=temp&0xE0;
		m_cLowByte=m_cLowByte&0x1F;
		m_cLowByte=m_cLowByte|temp;
	}
	void SetBlue(unsigned char blue){
		m_cLowByte=m_cLowByte&0xE0;
		blue=blue>>3;
		m_cLowByte=m_cLowByte|blue;
	}

	unsigned char m_cLowByte;
	unsigned char m_cHighByte;
#else
	EColor(unsigned char r=0,unsigned char g=0,unsigned char b=0):m_cRed(r),m_cGreen(g),m_cBlue(b){}
	unsigned char m_cRed;
	unsigned char m_cGreen;
	unsigned char m_cBlue;
#endif //EG_COMPACT_ECOLOR
};

/*
	The Definition of EEvent

	m_uSource  :  
		0    None =>  Idle Event
		1	 Cursor	
				m_uMessage  :    0     None 
				                 1     LeftClick
								 2     RightClick
								 3     LeftDoubleClick
								 4     RightDoubleClick
								 5     MidClick
				m_pPosition :    Cursor Position
		2	 Keyboard
				m_uMessage  :    Pressed Key
				m_pPosition :    Cursor Position

*/
struct EEvent{
	EEvent():m_uSource(0),m_uMessage(0),m_pPosition(0,0){}
	EEvent(unsigned int eventSource,unsigned int message)
		:m_uSource(eventSource),m_uMessage(message),m_pPosition(0,0){}
	EEvent(unsigned int eventSource,unsigned int message,unsigned int x,unsigned int y)
		:m_uSource(eventSource),m_uMessage(message),m_pPosition(x,y){}

	unsigned int m_uSource;
	unsigned int m_uMessage;
	EPosition m_pPosition;
};

